v1 = []  # holds the values for first row of viterbi which is for state at
v2 = []  # holds the values for second row of viterbi which is for state cg
pointers_v1 = []  # holds the state to which the pointer points to from the corresponding index value in v1
pointers_v2 = []  # holds the state to which the pointer points to from the corresponding index value in v2
state_list = []  # holds the list of most likely states


# Finds the maximum of all states in the previous time-step multiplied by the transition probability
# from that state
def max_j(path1, transition1, path2, transition2, states):
    if path1 == "-inf":
        option2 = path2 + np.log(transition2)
        return option2, states[1]

    option1 = path1 + np.log(transition1)
    if path2 == "-inf":
        return option1, states[0]

    option2 = path2 + np.log(transition2)
    maximum = max(option1, option2)

    if maximum == option1:
        return option1, states[0]
    else:
        return option2, states[1]


def viterbi(emissions, states, emission_probabilities_1, emission_probabilities_2, transitions):
    v0 = 'CG'#random.choices(states, weights=(0.5, 0.5))[0]  # the start state

    iterations = len(emissions) + 1

    for i in range(iterations):
        if i == 0 and v0 == states[0]:
            v1.append(0)
            v2.append("-inf")
        elif i == 0 and v0 == states[1]:
            v2.append(0)
            v1.append("-inf")
        else:
            # Randomly choosing an emission value
            emission = emissions[i - 1]

            # Previous time-step prob in state 1 (at), transition prob to state 1 from state 1 (at from at),
            # previous time-step prob in state 2 (cg), transition prob to state 2 from state 1 (cg from at)
            max_path_1 = max_j(v1[i - 1], transitions[0], v2[i - 1], transitions[1], states)
            # print ("max_path_1",max_path_1)

            # Previous time-step prob in state1 (at), transition prob to state 1 from state 2 (at from cg),
            # previous time-step prob in state 2 (cg), transition prob to state 2 to state 2 (cg from cg)
            max_path_2 = max_j(v1[i - 1], transitions[3], v2[i - 1], transitions[2], states)
            # print ("max_path_2",max_path_2)

            # probability of getting emission value in state 1 (at) + (the maximum of all states in the previous
            # time-step * by the transition probability from that state to state 1 (ag))
            v1_value = np.log(emission_probabilities_1[emission]) + max_path_1[0]

            # probability of getting emission value in state 2 (cg) + (the maximum of all states in the previous
            # time-step * by the transition probability from that state to state 2 (cg))
            v2_value = np.log(emission_probabilities_2[emission]) + max_path_2[0]

            if max_path_1[1] == states[0]:
                pointers_v1.append(states[0])
            else:
                pointers_v1.append(states[1])
            if max_path_2[1] == states[0]:
                pointers_v2.append(states[0])
            else:
                pointers_v2.append(states[1])

            # print("v1 value at ",i," ",v1_value)
            # print("v2 value at ",i," ",v2_value,"\n")
            v1.append(v1_value)
            v2.append(v2_value)

    if v1[len(v1) - 1] > v2[len(v1) - 1]:
        state_list.append(states[0])
    else:
        state_list.append(states[1])

    state_num = 0

    # print("pointers v1: ", pointers_v1)
    # print("pointers v2", pointers_v2)
    for i in range(len(v1) - 2, -1, -1):  # For loop will start from last index
        if state_list[state_num] == states[0]:
            state_list.append(pointers_v1[i])
        else:
            state_list.append(pointers_v2[i])
        state_num += 1

    state_list.reverse()  # Must reverse state list as it's created in reverse order

    # print(state_list)

    fig, ax1 = plt.subplots()
    ax1.set_title('Viterbi Algorithm')
    ax1.set_xlabel('Time')
    ax1.set_ylabel('States')

    xticks = [0]
    for i in range(1,len (state_list)):
        if state_list[i] != state_list[i - 1]:
            xticks.append(i)
        elif i == len (state_list)-1:
            xticks.append(len(state_list)-1)

    plt.xticks(rotation=90)
    ax1.set_xticks(xticks)
    ax1.plot(range(len(state_list)), state_list)#, marker='o')
    fig.set_size_inches(15, 5)
    # plt.grid(True)
    plt.show()


